// Generate available time slots for a specialty in a hospital
export const generateAvailableTimeSlots = (specialtyId: number, hospitalId: number) => {
  // Generate available dates for the next 14 days
  const availableDates = [];
  const today = new Date();
  
  for (let day = 0; day < 14; day++) {
    const date = new Date();
    date.setDate(today.getDate() + day);
    
    // Generate 8-12 time slots per day
    const slots = [];
    const busySlots = [];
    
    // Morning slots (8:00 - 11:30)
    for (let hour = 8; hour < 12; hour++) {
      for (let minute of [0, 30]) {
        const timeStr = `${hour.toString().padStart(2, '0')}:${minute.toString().padStart(2, '0')}`;
        
        // Randomly mark some slots as busy (about 30%)
        if (Math.random() < 0.3) {
          busySlots.push(timeStr);
        } else {
          slots.push(timeStr);
        }
      }
    }
    
    // Afternoon slots (13:30 - 17:30)
    for (let hour = 13; hour < 18; hour++) {
      for (let minute of [0, 30]) {
        // Skip 13:00 (lunch break)
        if (hour === 13 && minute === 0) continue;
        
        const timeStr = `${hour.toString().padStart(2, '0')}:${minute.toString().padStart(2, '0')}`;
        
        // Randomly mark some slots as busy (about 30%)
        if (Math.random() < 0.3) {
          busySlots.push(timeStr);
        } else {
          slots.push(timeStr);
        }
      }
    }
    
    availableDates.push({ date, slots, busySlots });
  }
  
  return availableDates;
};

